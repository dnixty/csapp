/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
  int count = 1;
  int n;
  x = x^(x>>31);

  x |= x>>16;
  x |= x>>8;
  x |= x>>4;
  x |= x>>2;
  x |= x>>1;

  n = (!!(x&((0xff<<24)|(0xff<<16))))<<4;
  x >>= n;
  count += n;

  n = (!!(x&(0xff<<8)))<<3;
  x >>= n;
  count += n;

  n = (!!(x&0xf0))<<2;
  x >>= n;
  count += n;

  n = (!!(x&0xc))<<1;
  x >>= n;
  count += n;

  n = (!!(x&0x2));
  x >>= n;
  count += n;

  n = (!!(x&0x1));
  count += n;

  return count;
}

